DATA SEGMENT
BLOCK1 DB 'niwin'
MSG1 DB "IT IS A PALINDROME$"
MSG2 DB "IT IS NOT PALINDROME$"
DATA ENDS

PRINT MACRO MSG
MOV AH, 09H
LEA DX, MSG
INT 21H
MOV AH, 4CH
INT 21H
ENDM

EXTRA SEGMENT
BLOCK2 DB 09 DUP(?)
EXTRA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA, ES:EXTRA
START:  MOV AX, DATA
        MOV DS, AX
        MOV AX, EXTRA
        MOV ES, AX
        LEA SI, BLOCK1
        LEA DI, BLOCK2+8
        MOV CX, 09H
BACK:   CLD

        LODSB
        STD

        STOSB

        LOOP BACK
        LEA SI, BLOCK1
        LEA DI, BLOCK2
        MOV CX, 09H
        CLD
        REPZ CMPSB

        JNZ SKIP
        PRINT MSG1

SKIP:   PRINT MSG2

        CODE ENDS
        END START

